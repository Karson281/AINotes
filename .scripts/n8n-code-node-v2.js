// Get the first webhook input item
const input = $input.first().json;

// If webhook sent the data wrapped differently, try the body
const data = input.body || input;

const stocks = data.stocks || [];
const summary = data.summary || {};
const hsi = data.hsi || null;

if (!stocks.length) {
  return [{ json: { message: '⚠️ 無股票數據' } }];
}

// === Build message ===
let msg = `<b>📊 每日股票報告</b>\n📅 ${data.date || ''} ${data.timestamp || ''}\n`;

// HSI index
if (hsi && hsi.price) {
  const sign = parseFloat(hsi.change) >= 0 ? '+' : '';
  const emoji = parseFloat(hsi.change) >= 0 ? '🟢' : '🔴';
  msg += `${emoji} 恆生指數: <b>${hsi.price}</b> (${sign}${hsi.change}%)\n`;
}

// Summary line
msg += `\n📊 <b>概要</b>\n`;
if (summary.positive !== undefined) {
  msg += `🟢 上升: ${summary.positive}  |  🔴 下跌: ${summary.negative}  |  ⚪ 持平: ${stocks.length - summary.positive - summary.negative}\n`;
  msg += `⭐ 買入信號: ${summary.buy_signals}  |  👀 觀望: ${summary.watch}  |  ⚠️ 賣出信號: ${summary.sell_signals}\n`;
}

// === Hong Kong stocks ===
const hk = stocks.filter(s => (s.region || '').includes('HK') || (s.ticker || '').endsWith('.HK'));
if (hk.length) {
  msg += `\n<b>🔹 港股</b>\n`;
  hk.forEach(s => {
    const pct = parseFloat(s.change_percent || 0);
    const sign = pct >= 0 ? '+' : '';
    const emoji = pct > 0.3 ? '🟢' : pct > -0.3 ? '⚪' : '🔴';
    msg += `${emoji} <b>${s.name}</b> (${s.ticker})\n`;
    msg += `   💰 ${s.price} (${sign}${s.change_percent}%)  ⭐ ${s.rating || '—'}\n`;
  });
}

// === US stocks ===
const us = stocks.filter(s => !((s.region || '').includes('HK') || (s.ticker || '').endsWith('.HK')));
if (us.length) {
  msg += `\n<b>🔹 美股</b>\n`;
  us.forEach(s => {
    const pct = parseFloat(s.change_percent || 0);
    const sign = pct >= 0 ? '+' : '';
    const emoji = pct > 0.3 ? '🟢' : pct > -0.3 ? '⚪' : '🔴';
    msg += `${emoji} <b>${s.name}</b> (${s.ticker})\n`;
    msg += `   💰 $${s.price} (${sign}${s.change_percent}%)  ⭐ ${s.rating || '—'}\n`;
  });
}

msg += `\n<i>🤖 Generated by Hermes → n8n</i>`;

return [{ json: { message: msg } }];
