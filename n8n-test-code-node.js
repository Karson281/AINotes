// === Helper: change emoji ===
function changeEmoji(pct) {
  if (pct > 0.5) return '🟢';
  if (pct > 0) return '🟢';
  if (pct > -0.5) return '🔴';
  return '🔴';
}

// === Build message ===
let msg = `<b>📊 每日股票報告</b>\n📅 ${dateStr} ${timeStr} HKT\n\n`;

// Hong Kong stocks
msg += `<b>🔹 港股</b>\n`;
const hkStocks = stocks.filter(s => s.region === 'HK');
hkStocks.forEach(s => {
  const sign = s.change_pct >= 0 ? '+' : '';
  msg += `${changeEmoji(s.change_pct)} <b>${s.name}</b> (${s.ticker})\n`;
  msg += `   💰 ${s.price.toFixed(2)} (${sign}${s.change_pct.toFixed(2)}%)  ⭐ ${s.rating}\n`;
});

// US stocks
msg += `\n<b>🔹 美股</b>\n`;
const usStocks = stocks.filter(s => s.region === 'US');
usStocks.forEach(s => {
  const sign = s.change_pct >= 0 ? '+' : '';
  msg += `${changeEmoji(s.change_pct)} <b>${s.name}</b> (${s.ticker})\n`;
  msg += `   💰 $${s.price.toFixed(2)} (${sign}${s.change_pct.toFixed(2)}%)  ⭐ ${s.rating}\n`;
});

msg += `\n<i>🤖 Generated by Hermes → n8n</i>`;

